def addition(op1,op2):
    return op1+op2
def subtraction(op1, op2):
    return op1-op2
def multiply(op1, op2):
    return op1*op2
def power(op1,op2):
    return op1**op2
def divide(op1,op2):
    return op1/op2
def integer_divide(op1,op2):
    return op1//op2
print(addition(45,45))
print(subtraction(45,45))
print(multiply(45,45))
print(power(2,5))
print(integer_divide(16,3))
print(divide(16,3))